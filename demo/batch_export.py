import argparse
import json
import re
import sys
from pathlib import Path

import numpy as np


KNOWN_MODELS   = {"arima", "xgboost", "itransformer", "timemixer", "tsmamba"}
KNOWN_HORIZONS = {12, 24, 48}
KNOWN_DATASETS = {
    "sunspots", "appliances_energy",
    "beijing_air_quality", "hanoi_air_quality", "bitcoin",
}


def parse_filename(stem):
    stem = re.sub(r"_predictions$", "", stem)
    parts = stem.split("_")
    if len(parts) < 3:
        return None, None, None

    model   = parts[0]
    try:
        horizon = int(parts[1])
    except ValueError:
        return None, None, None
    dataset = "_".join(parts[2:])

    if model not in KNOWN_MODELS:
        return None, None, None
    if horizon not in KNOWN_HORIZONS:
        return None, None, None
    if dataset not in KNOWN_DATASETS:
        return None, None, None

    return model, horizon, dataset


def smart_3d(arr, n_tc=None):
    if arr.ndim == 3:
        return arr
    if arr.ndim == 2:
        if n_tc is not None and arr.shape[1] == n_tc:
            return arr[:, np.newaxis, :]
        else:
            return arr[:, :, np.newaxis]
    return arr[:, np.newaxis, np.newaxis]


def to_points(arr_1d, start_i=0):
    return [{"i": start_i + k, "label": f"t{start_i + k}", "v": round(float(v), 6)}
            for k, v in enumerate(arr_1d)]


def compute_metrics(actual, predicted):
    a, p = np.asarray(actual, float), np.asarray(predicted, float)
    err   = a - p
    mae   = float(np.mean(np.abs(err)))
    rmse  = float(np.sqrt(np.mean(err ** 2)))
    wmape = float(np.sum(np.abs(err)) / (np.sum(np.abs(a)) + 1e-8))
    ss_tot = float(np.sum((a - a.mean()) ** 2))
    r2    = float(1 - np.sum(err ** 2) / ss_tot) if ss_tot > 0 else 0.0
    return {"mae": mae, "rmse": rmse, "wmape": wmape, "r2": r2}


_HEADER = """\
/* =============================================================================
   predictions.js  —  auto-generated by batch_export.py
   Re-run batch_export.py to regenerate.

   Key format:  "<model>|<dataset>|<horizon>|<target>|<windowIndex>"
   ========================================================================== */
"""


def read_existing(js_path):
    if not js_path.exists():
        return {}
    text = js_path.read_text(encoding="utf-8")
    m = re.search(r"window\.PREDICTIONS\s*=\s*(\{.*\})\s*;", text, re.DOTALL)
    if m:
        try:
            return json.loads(m.group(1))
        except json.JSONDecodeError:
            pass
    return {}


def write_predictions(js_path, predictions):
    body = json.dumps(predictions, indent=2, ensure_ascii=False)
    js_path.write_text(_HEADER + f"window.PREDICTIONS = {body};\n", encoding="utf-8")


def process_file(npz_path, window_index, predictions, dl_full_len=None, dl_n_windows=None):
    stem = npz_path.stem  # e.g. "arima_12_sunspots_predictions"
    model, horizon, dataset = parse_filename(stem)

    if model is None:
        return False, f"SKIP  {npz_path.name}  (không nhận diện được tên file)"

    try:
        data = np.load(npz_path, allow_pickle=True)
    except Exception as e:
        return False, f"ERROR {npz_path.name}  ({e})"

    if "target_cols" in data:
        target_cols = [str(c) for c in data["target_cols"]]
        n_tc = len(target_cols)
    else:
        target_cols = None
        n_tc = None

    try:
        y_pred = smart_3d(data["y_pred"], n_tc)
        y_true = smart_3d(data["y_true"], n_tc)
    except KeyError as e:
        available = list(data.files)
        return False, f"ERROR {npz_path.name}  (key {e} not found — available: {available})"

    if target_cols is None:
        target_cols = [f"target_{i}" for i in range(y_pred.shape[2])]

    n_windows, pred_len, n_targets = y_pred.shape

    rolling = (pred_len == 1 and horizon > 1)

    if rolling:
        max_wi = n_windows - horizon - 1
    else:
        max_wi = n_windows - 1

    wi_key = window_index
    wi     = max_wi + 1 + window_index if window_index < 0 else window_index
    wi     = max(0, min(wi, max_wi))

    added = []
    for ti, tname in enumerate(target_cols):
        if ti >= n_targets:
            break

        N_HIST = 48 

        if rolling:
            pa = y_pred[wi : wi + horizon, 0, ti]
            ta = y_true[wi : wi + horizon, 0, ti]
            hist_start = max(0, wi - N_HIST)
            ha = y_true[hist_start : wi, 0, ti]
        else:
            pa = y_pred[wi, :, ti]
            ta = y_true[wi, :, ti]
            hist_start = max(0, wi - N_HIST)
            ha = y_true[hist_start : wi, 0, ti]

        if rolling:
            all_true = y_true[:, 0, ti]
            all_pred = y_pred[:, 0, ti]
        else:
            all_true = y_true[:, :, ti].reshape(-1)
            all_pred = y_pred[:, :, ti].reshape(-1)
        metrics = compute_metrics(all_true, all_pred)

        hist_start_i = 48 - len(ha)
        key = f"{model}|{dataset}|{horizon}|{tname}|{wi_key}"
        predictions[key] = {
            "history":   to_points(ha, start_i=hist_start_i),
            "actual":    to_points(ta, start_i=48),
            "predicted": to_points(pa, start_i=48),
            "metrics":   metrics,
        }

        N_FULL  = 500
        SEQ_LEN = 48
        dl_clip = (dl_full_len  or {}).get((dataset, horizon), None)
        dl_nw   = (dl_n_windows or {}).get((dataset, horizon), None)
        arima_rolling = rolling and (dl_nw is None or n_windows > dl_nw)
        if rolling:
            if arima_rolling:
                align = SEQ_LEN
            else:
                align = 0   
            cap = min(N_FULL, dl_clip) if dl_clip else min(N_FULL, n_windows - align)
            _t = y_true[align : align + cap, 0, ti]
            _p = y_pred[align : align + cap, 0, ti]
            full_variants = {
                "full":       (_t, _p),
                "full_first": (_t, _p),
                "full_last":  (_t, _p),
            }
        else:
            sel      = np.arange(0, n_windows, pred_len)
            cap      = min(N_FULL, len(sel) * pred_len)
            non_ov_t = y_true[sel, :, ti].reshape(-1)[:cap]
            non_ov_p = y_pred[sel, :, ti].reshape(-1)[:cap]
            first_t  = y_true[:cap, 0,  ti]
            first_p  = y_pred[:cap, 0,  ti]
            last_t   = y_true[:cap, -1, ti]
            last_p   = y_pred[:cap, -1, ti]
            full_variants = {
                "full":       (non_ov_t, non_ov_p),
                "full_first": (first_t,  first_p),
                "full_last":  (last_t,   last_p),
            }
        if rolling:
            variant_start = {"full": horizon - 1, "full_first": horizon - 1, "full_last": horizon - 1}
        else:
            variant_start = {"full": 0, "full_first": 0, "full_last": horizon - 1}
        for suffix, (ft, fp) in full_variants.items():
            si = variant_start[suffix]
            predictions[f"{model}|{dataset}|{horizon}|{tname}|{suffix}"] = {
                "actual":    to_points(ft, start_i=si),
                "predicted": to_points(fp, start_i=si),
                "metrics":   metrics,
            }

        mode_tag = " [arima rolling]" if arima_rolling else (" [last-step only]" if rolling else "")
        added.append(f"{tname}  MAE={metrics['mae']:.4f}  R²={metrics['r2']:.4f}{mode_tag}")

    summary = f"OK    {npz_path.name}  →  {model}/{dataset}/h{horizon}  ({len(added)} targets)"
    for line in added:
        summary += f"\n        {line}"
    return True, summary


def main():
    ap = argparse.ArgumentParser(description="Batch export 75 .npz files → predictions.js")
    ap.add_argument("--dir",    default="../predictions", help="Directory containing *_predictions.npz files (default: ../predictions)")
    ap.add_argument("--window", type=int, default=-1,
                    help="Test window index")
    _default_out = str(Path(__file__).parent / "ui" / "web" / "predictions.js")
    ap.add_argument("--out",    default=_default_out,
                    help="Output file (default: <script_dir>/ui/web/predictions.js)")
    ap.add_argument("--overwrite", action="store_true",
                    help="Overwrite predictions.js")
    args = ap.parse_args()

    npz_dir = Path(args.dir)
    if not npz_dir.is_dir():
        sys.exit(f"ERROR: {npz_dir} does not exists.")

    files = sorted(npz_dir.glob("**/*_predictions.npz"))
    if not files:
        sys.exit(f"Can't find *_predictions.npz in {npz_dir}")

    out_path = Path(args.out)
    predictions = {} if args.overwrite else read_existing(out_path)

    print(f"Found {len(files)} file .npz in {npz_dir}")
    print(f"Output → {out_path}  (window={args.window})\n")

    dl_full_len = {}
    dl_n_windows = {}
    for f in files:
        model, horizon, dataset = parse_filename(f.stem)
        if model is None or model == "arima":
            continue
        try:
            d = np.load(f, allow_pickle=True)
            yt = smart_3d(d["y_true"])
            n_windows, pred_len, _ = yt.shape
            key = (dataset, horizon)
            # dl_n_windows: minimum n_windows across all non-ARIMA models
            dl_n_windows[key] = min(dl_n_windows.get(key, n_windows), n_windows)
            if pred_len > 1:
                sel    = np.arange(0, n_windows, pred_len)
                length = min(500, len(sel) * pred_len)
                dl_full_len[key] = min(dl_full_len.get(key, length), length)
        except Exception:
            pass
    print(f"Pass 1 done — {len(dl_full_len)} DL full lengths, {len(dl_n_windows)} n_windows collected")

    ok_count = skip_count = err_count = 0
    for f in files:
        ok, msg = process_file(f, args.window, predictions, dl_full_len, dl_n_windows)
        print(msg)
        if ok:
            ok_count += 1
        elif msg.startswith("SKIP"):
            skip_count += 1
        else:
            err_count += 1

    print(f"\n{'─'*60}")
    print(f"Result: {ok_count} OK  /  {skip_count} SKIP  /  {err_count} ERROR")
    print(f"Total entries in predictions.js: {len(predictions)}")

    write_predictions(out_path, predictions)
    print(f"Wrote → {out_path}")

    if err_count:
        print(f"\n {err_count} error files")
        sys.exit(1)


if __name__ == "__main__":
    main()
